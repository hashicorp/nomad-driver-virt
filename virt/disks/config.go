// generateIdentifier generates an identifier for a source file based
// on the checksum of the contents
func generateIdentifier(path, format string) (string, error) {
	hash := sha256.New()
	f, err := os.Open(path)
	if err != nil {
		return "", err
	}
	defer f.Close()

	if _, err := io.Copy(hash, f); err != nil {
		return "", err
	}

	return fmt.Sprintf("%s-%s-%x.img", identifierPrefix, format, hash.Sum(nil)), nil
}

// fileExists checks if a file exists at the path
func fileExists(path string) bool {
	info, err := os.Stat(path)
	if err != nil {
		return false
	}

	return !info.IsDir()
}

// prepareNomadVolume will copy an image file into the block device provided
// by the nomad volume backing the disk.
func (d *Disk) writeNomadVolume(s storage.Storage) error {
	if d.blockDevicePath == "" {
		return fmt.Errorf("Disk is not backed by Nomad volume")
	}

	// Do nothing if no source is defined
	if d.Source == nil || d.Source.Image == "" {
		return nil
	}

	// Make sure image is in correct format
	img := d.Source.Image
	if d.Format != d.Source.Format {
		rawImg := img + ".raw"
		if err := s.ImageHandler().ConvertImage(img, d.Source.Format, rawImg, d.Format); err != nil {
			return err
		}
		defer os.RemoveAll(rawImg)
		img = rawImg
	}

	// Write the image to the device
	src, err := os.Open(img)
	if err != nil {
		return err
	}
	defer src.Close()

	dst, err := os.OpenFile(d.blockDevicePath, os.O_WRONLY, 0600)
	if err != nil {
		return err
	}
	defer dst.Close()

	if _, err := io.Copy(dst, src); err != nil {
		return err
	}

	return nil
}

// prefixError prepends the prefix to the error. If the error is a multierror.Error
// it will prefix all error messages it contains.
func prefixError(prefix string, err error) error {
	if err == nil {
		return err
	}

	switch err := err.(type) {
	case *multierror.Error:
		var mErr *multierror.Error
		if err == nil {
			return err
		}
		for _, e := range err.Errors {
			if !strings.Contains(e.Error(), prefix) {
				e = fmt.Errorf("%s %w", prefix, e)
			}
			mErr = multierror.Append(mErr, e)
		}
		return mErr
	default:
		return fmt.Errorf("%s %w", prefix, err)
	}
}

// convertImage converts an image file into a different format, creating the
// converted file next to the original.
func convertImage(h image_tools.ImageHandler, src, srcFmt, dstFmt string) (string, error) {
	dir := filepath.Dir(src)
	f, err := os.CreateTemp(dir, fmt.Sprintf("%s-*.%s", filepath.Base(src), dstFmt))
	if err != nil {
		return "", err
	}
	f.Close()

	if err := h.ConvertImage(src, srcFmt, f.Name(), dstFmt); err != nil {
		return "", err
	}

	return f.Name(), nil
}
